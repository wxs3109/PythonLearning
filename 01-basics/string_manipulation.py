# String Manipulation


s = "Hello, World!    "
s.upper() # returns "HELLO, WORLD!"
s.lower() # returns "hello, world!"
s.capitalize() # returns "Hello, world!"
s.title() # returns "Hello, World!"
s.strip() # returns "Hello, World!"
s.split() # returns ["Hello,", "World!"]
s.join() # returns "Hello, World!"
s.replace() # returns "Hello, World!"
s.find()